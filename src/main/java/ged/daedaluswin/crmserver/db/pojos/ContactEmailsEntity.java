package ged.daedaluswin.crmserver.db.pojos;

import javax.persistence.*;

/**
 * Created by Mercutio Donnati on 1/4/2015.
 */
@Entity
@Table(name = "ContactEmails", schema = "crm", catalog = "CRM_UAT")
public class ContactEmailsEntity {
    private int id;
    private String email;

    @Id
    @Column(name = "ID")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "Email")
    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        ContactEmailsEntity that = (ContactEmailsEntity) o;

        if (id != that.id) return false;
        if (email != null ? !email.equals(that.email) : that.email != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (email != null ? email.hashCode() : 0);
        return result;
    }
}
